import streamlit as st

st.set_page_config(page_title="–°–∏—Å—Ç–µ–º–∞ –∑–∞ –æ—Ü–µ–Ω–∫–∏", page_icon="üéì")

st.title("üéì –°–∏—Ç–µ–º–∞ –∑–∞ –æ—Ü–µ–Ω—è–≤–∞–Ω–µ")
st.markdown("–í—ä–≤–µ–¥–µ—Ç–µ —Ç–æ—á–∫–∏—Ç–µ –æ—Ç –∏–∑–ø–∏—Ç–∞, –∑–∞ –¥–∞ –∏–∑—á–∏—Å–ª–∏—Ç–µ —Ñ–∏–Ω–∞–ª–Ω–∏—è —Ä–µ–∑—É–ª—Ç–∞—Ç.")

# –ò–∑–ø–æ–ª–∑–≤–∞–º–µ –∫–æ–ª–æ–Ω–∏ –∑–∞ –ø–æ-–¥–æ–±—ä—Ä –∏–∑–≥–ª–µ–¥
col1, col2 = st.columns(2)

with col1:
    theory = st.number_input("–¢–µ–æ—Ä–∏—è (0-50):", min_value=0, max_value=100, value=0)

with col2:
    practical = st.number_input("–ü—Ä–∞–∫—Ç–∏–∫–∞ (0-50):", min_value=0, max_value=100, value=0)

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞ –≥—Ä–∞–Ω–∏—Ü–∏—Ç–µ
if theory > 50 or practical > 50:
    st.error("‚ö†Ô∏è –ì—Ä–µ—à–∫–∞: –ú–∞–∫—Å–∏–º–∞–ª–Ω–∏—è—Ç –±—Ä–æ–π —Ç–æ—á–∫–∏ –∑–∞ –≤—Å–µ–∫–∏ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç –µ 50!")
else:
    total = theory + practical
    percentage = (total / 100)
    
    st.divider() # –•–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–Ω–∞ –ª–∏–Ω–∏—è
    
    # –ü–æ–∫–∞–∑–≤–∞–Ω–µ –Ω–∞ –æ–±—â–∏—è —Ä–µ–∑—É–ª—Ç–∞—Ç
    st.subheader(f"–û–±—â –±—Ä–æ–π —Ç–æ—á–∫–∏: {total} / 100")
    st.progress(percentage)

    # –õ–æ–≥–∏–∫–∞ –∑–∞ –æ–ø—Ä–µ–¥–µ–ª—è–Ω–µ –Ω–∞ –æ—Ü–µ–Ω–∫–∞—Ç–∞
    if total >= 90:
        grade = "–û—Ç–ª–∏—á–µ–Ω (6)"
        st.success(f"–ü–æ–∑–¥—Ä–∞–≤–ª–µ–Ω–∏—è! –í–∞—à–∞—Ç–∞ –æ—Ü–µ–Ω–∫–∞ –µ: **{grade}** üåü")
    elif total >= 75:
        grade = "–ú–Ω–æ–≥–æ –¥–æ–±—ä—Ä (5)"
        st.info(f"–ú–Ω–æ–≥–æ –¥–æ–±—ä—Ä —Ä–µ–∑—É–ª—Ç–∞—Ç: **{grade}** üëç")
    elif total >= 50:
        grade = "–î–æ–±—ä—Ä (4)"
        st.warning(f"–†–µ–∑—É–ª—Ç–∞—Ç—ä—Ç –≤–∏ –µ: **{grade}** üëå")
    elif total >= 30:
        grade = "–°—Ä–µ–¥–µ–Ω (3)"
        st.warning(f"–†–µ–∑—É–ª—Ç–∞—Ç—ä—Ç –≤–∏ –µ: **{grade}** üòê")
    else:
        grade = "–°–ª–∞–± (2)"
        st.error(f"–ó–∞ —Å—ä–∂–∞–ª–µ–Ω–∏–µ –Ω–µ –∏–∑–¥—ä—Ä–∂–∞—Ö—Ç–µ. –û—Ü–µ–Ω–∫–∞: **{grade}** ‚ùå")

    # –î–æ–ø—ä–ª–Ω–∏—Ç–µ–ª–Ω–∞ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ (expandable section)
    with st.expander("–í–∏–∂ –¥–µ—Ç–∞–π–ª–∏"):
        st.write(f"- –ü—Ä–æ—Ü–µ–Ω—Ç —É—Å–ø–µ–≤–∞–µ–º–æ—Å—Ç: {total}%")
        st.write(f"- –û—Å—Ç–∞–≤–∞—Ç –¥–æ 100 —Ç–æ—á–∫–∏: {100 - total}")
